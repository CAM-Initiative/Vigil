def generated_summaries(record: dict[str, Any]) -> dict[str, Any]:
    record_type = record.get("record_type", "")
    summaries: dict[str, Any] = {
        "source_summary": source_summary(record),
        "system_summary": system_summary(record),
        "jurisdiction_summary": jurisdiction_summary(record),
    }

    if record_type == "observation":
        summaries.update(
            {
                "possible_taxonomy_mapping_summary": possible_taxonomy_mapping_summary(record),
                "cam_summary": observation_cam_summary(record),
                "next_action": record.get("next_action", ""),
                "review_observation": record.get("review_observation", ""),
            }
        )
    elif record_type == "failure_mode":
        summaries.update(
            {
                "classification_summary": classification_summary(record),
                "triage_summary": triage_summary(record),
                "failure_mode_definition_summary": record.get("failure_mode_definition", ""),
                "failure_threshold_summary": record.get("failure_threshold", ""),
                "cam_summary": failure_cam_summary(record),
            }
        )
    elif record_type == "proposal":
        summaries.update(
            {
                "proposal_summary": proposal_summary(record),
                "external_relevance_summary": external_relevance_summary(record),
                "cam_summary": proposal_cam_summary(record),
                "implementation_notes_summary": implementation_notes_summary(record),
                "next_action": record.get("next_action", ""),
            }
        )
    elif record_type in {"patch", "patch_note"}:
        summaries.update(
            {
                "change_summary": change_summary(record),
                "verification_summary": verification_summary(record),
                "impact_summary": impact_summary(record),
                "cam_summary": patch_cam_summary(record),
                "remaining_work": record.get("remaining_work", []),
            }
        )

    return prune_empty(summaries)


def write_json(path: Path, data: Any) -> None:
    pruned = prune_empty(data)
    if isinstance(data, dict) and "records" in data and isinstance(pruned, dict):
        pruned.setdefault("records", [])
    path.write_text(json.dumps(pruned, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def canonical_sources(record: dict[str, Any]) -> list[dict[str, Any]]:
    sources = record.get("source_records", [])
    return [source for source in sources if isinstance(source, dict)] if isinstance(sources, list) else []


def source_summary(record: dict[str, Any]) -> dict[str, Any]:
    sources = canonical_sources(record)
    primary = sources[0] if sources else {}
    types = source_types(record)
    platforms = sorted(
        {source.get("source_platform") for source in sources if source.get("source_platform")}
    )
    return {
        "source_count": len(sources),
        "primary_source_title": primary.get("source_title", ""),
        "primary_source_type": primary.get("source_type", ""),
        "primary_source_platform": primary.get("source_platform", ""),
        "primary_source_author_or_publisher": primary.get("author_or_publisher", ""),
        "primary_source_date": primary.get("source_date", ""),
        "primary_source_url": primary.get("source_url", ""),
        "primary_archive_url": primary.get("archive_url", ""),
        "source_types": types,
        "source_platforms": platforms,
        "source_url_status": primary.get("source_url_status", ""),
        "evidence_confidence": record.get("evidence_confidence", ""),
    }


def source_types(record: dict[str, Any]) -> list[str]:
    return sorted({source.get("source_type") for source in canonical_sources(record) if source.get("source_type")})


def record_path(record: dict[str, Any]) -> str:
    source_path = record.get("__source_path")
    if isinstance(source_path, Path):
        return repo_relative_path(source_path)
    record_id = record.get("id", "")
    for path in record_files():
        if path.stem == record_id:
            return repo_relative_path(path)
    return ""


def aggregate_record(record: dict[str, Any]) -> dict[str, Any]:
    entry = {key: value for key, value in record.items() if key != "__source_path"}
    entry.update(generated_summaries(record))
    path = record_path(record)
    if path:
        entry.update({"path": path, "github_blob_url": github_blob_url(path), "raw_url": raw_url(path)})
    return entry


def record_title(record: dict[str, Any]) -> str:
    identity = record.get("record_identity", {}) if isinstance(record.get("record_identity"), dict) else {}
    for value in (
        record.get("title"),
        identity.get("title"),
        record.get("summary"),
        record.get("id"),
    ):
        if value:
            return str(value)
    return ""


def list_metadata(record: dict[str, Any]) -> dict[str, Any]:
    """Return the lean collapsed-list metadata used by generated index entries.

    Detailed summaries remain available in the canonical record JSON and in legacy
    aggregate helpers, but generated indexes intentionally expose only list,
    search, filter, sort, and canonical-location fields.
    """
    sources = canonical_sources(record)
    primary_source = sources[0] if sources else {}
    system = record.get("system_context") if isinstance(record.get("system_context"), dict) else {}
    jurisdiction = (
        record.get("jurisdictional_context")
        if isinstance(record.get("jurisdictional_context"), dict)
        else {}
    )
    classification = (
        record.get("failure_classification")
        if isinstance(record.get("failure_classification"), dict)
        else {}
    )
    triage = record.get("triage") if isinstance(record.get("triage"), dict) else {}
    proposal_scope = (
        record.get("proposal_scope") if isinstance(record.get("proposal_scope"), dict) else {}
    )
    change_classification = (
        record.get("change_classification")
        if isinstance(record.get("change_classification"), dict)
        else {}
    )
    verification = (
        record.get("implementation_verification")
        if isinstance(record.get("implementation_verification"), dict)
        else {}
    )

    return {
        "source_count": len(sources),
        "primary_source_title": primary_source.get("source_title", ""),
        "primary_source_type": primary_source.get("source_type", ""),
        "primary_source_platform": primary_source.get("source_platform", ""),
        "source_platforms": sorted(
            {source.get("source_platform") for source in sources if source.get("source_platform")}
        ),
        "source_url_status": primary_source.get("source_url_status", ""),
        "system_type": system.get("system_type", ""),
        "platform_or_vendor": system.get("platform_or_vendor", ""),
        "vendor_cluster": system.get("vendor_cluster", []),
        "primary_evidenced_vendors": system.get("primary_evidenced_vendors", []),
        "product_or_service": system.get("product_or_service", ""),
        "specific_model_or_runtime": system.get("specific_model_or_runtime", ""),
        "model_or_product": system.get("model_or_product", ""),
        "interface_surface": system.get("interface_surface", ""),
        "deployment_context": system.get("deployment_context", ""),
        "primary_jurisdiction": jurisdiction.get("primary_jurisdiction", ""),
        "regulatory_surface": jurisdiction.get("regulatory_surface", []),
        "sector": jurisdiction.get("sector", ""),
        "canonical_failure_group": classification.get("canonical_failure_group")
        or change_classification.get("canonical_failure_group", ""),
        "failure_family": classification.get("failure_family", ""),
        "failure_subtype": classification.get("failure_subtype", ""),
        "severity": classification.get("severity", ""),
        "likelihood": classification.get("likelihood", ""),
        "triage_priority": triage.get("triage_priority", ""),
        "triage_status": triage.get("triage_status", ""),
        "mitigation_status": triage.get("mitigation_status", ""),
        "escalation_required": triage.get("escalation_required", ""),
        "proposal_type": record.get("proposal_type", []),
        "proposal_status": record.get("proposal_status", ""),
        "proposal_scope_summary": proposal_scope.get("scope_summary", ""),
        "patch_type": change_classification.get("patch_type")
        or change_classification.get("change_type", ""),
        "change_type": change_classification.get("change_type", ""),
        "change_family": change_classification.get("change_family")
        or change_classification.get("patch_family", ""),
        "change_subtype": change_classification.get("change_subtype", ""),
        "change_status": change_classification.get("change_status", ""),
        "implementation_state": verification.get("implementation_state")
        or verification.get("status", ""),
        "verification_status": verification.get("verification_status")
        or verification.get("status", ""),
    }


def index_record(record: dict[str, Any]) -> dict[str, Any]:
    path = record_path(record)
    entry = {
        "id": record.get("id", ""),
        "record_type": record.get("record_type", ""),
        "record_state": record.get("record_state", ""),
        "date_recorded": record.get("date_recorded", ""),
        "date_implemented": record.get("date_implemented", ""),
        "title": record_title(record),
        "summary": record.get("summary", ""),
        "evidence_confidence": record.get("evidence_confidence", ""),
        "source_types": source_types(record),
        **list_metadata(record),
        "path": path,
        "github_blob_url": github_blob_url(path) if path else "",
        "raw_url": raw_url(path) if path else "",
    }
    return entry


def master_record(index_entry: dict[str, Any]) -> dict[str, Any]:
    return index_entry


def type_registry(registry_type: str, records: list[dict[str, Any]]) -> dict[str, Any]:
    config = TYPE_CONFIG[registry_type]
    return {
        "generated_notice": NOTICE,
        "registry_type": registry_type,
        "generated_from": f"vigil/records/{config['directory']}/",
        "record_count": len(records),
        "records": [index_record(record) for record in records],
    }


def records_by_registry(records: list[dict[str, Any]]) -> dict[str, list[dict[str, Any]]]:
    grouped = {registry_type: [] for registry_type in TYPE_CONFIG}
    for record in records:
        registry_type = RECORD_TYPE_TO_REGISTRY.get(str(record.get("record_type", "")))
        if registry_type:
            grouped[registry_type].append(record)
    for registry_records in grouped.values():
        registry_records.sort(key=lambda record: str(record.get("id", "")))
    return grouped


def build_master_from_type_indexes(index_paths: dict[str, Path] | None = None) -> dict[str, Any]:
    paths = index_paths or OUTPUT_PATHS
    registries: dict[str, Any] = {}
    counts: dict[str, int] = {}
    records: list[dict[str, Any]] = []

    for registry_type in TYPE_CONFIG:
        path = paths[registry_type]
        registry = json.loads(path.read_text(encoding="utf-8"))
        rel_path = repo_relative_path(path)
        count = int(registry.get("record_count", 0))
        counts[registry_type] = count
        registries[registry_type] = {
            "path": rel_path,
            "github_blob_url": github_blob_url(rel_path),
            "raw_url": raw_url(rel_path),
            "record_count": count,
        }
        records.extend(master_record(record) for record in registry.get("records", []) if isinstance(record, dict))

    counts["total"] = sum(counts.values())
    generated_from = [repo_relative_path(paths[registry_type]) for registry_type in TYPE_CONFIG]
    return {
        "generated_notice": NOTICE,
        "registry_type": "vigil_registry_master",
        "generated_from": generated_from,
        "registry_count": len(TYPE_CONFIG),
        "record_count": counts,
        "registries": registries,
        "records": sorted(records, key=lambda record: str(record.get("id", ""))),
    }


def remove_deprecated_outputs() -> None:
    for path in DEPRECATED_OUTPUT_PATHS:
        path.unlink(missing_ok=True)


def build() -> None:
    remove_deprecated_outputs()
    grouped = records_by_registry(load_records())
    for registry_type, records in grouped.items():
        output_path = OUTPUT_PATHS[registry_type]
        write_json(output_path, type_registry(registry_type, records))
        print(f"Wrote {output_path} with {len(records)} records.")

    master = build_master_from_type_indexes()
    write_json(MASTER_OUTPUT_PATH, master)
    print(f"Wrote {MASTER_OUTPUT_PATH} with {master['record_count']['total']} records.")


if __name__ == "__main__":
    build()
